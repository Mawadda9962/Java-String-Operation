public class StringCheckDemo {
    public static void main(String[] args){

        String title = "Java Programming Language";

        System.out.println(title);


        System.out.println(title.substring(4));

        System.out.println(title.replace("Java", "C++"));

        System.out.println(title.contains("Java"));

        System.out.println(title.charAt(1));

        String title2 = "C++ Programming Language";

        System.out.println(title2);

        System.out.println(title.concat(title2));





    }
}
