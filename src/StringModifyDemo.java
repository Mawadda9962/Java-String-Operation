public class StringModifyDemo {
    public static void main(String[] args){

        String fullName = "  Mawadda Ahmed Alshukaili  ";

        System.out.println(fullName);

        System.out.println(fullName.trim());

        System.out.println(fullName.contains("Mawadda"));

        System.out.println(fullName.replace("Mawadda", "Nasser"));






    }
}
